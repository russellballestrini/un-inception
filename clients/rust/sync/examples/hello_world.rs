// Hello World example - standalone version
//
// This example demonstrates basic synchronous execution patterns.
// Shows how to execute code from a Rust program (simulated).
//
// To run:
//     rustc hello_world.rs && ./hello_world
//
// Expected output:
//     Executing code synchronously...
//     Result status: completed
//     Output: Hello from unsandbox!

fn main() {
    // Simulated code execution
    println!("Executing code synchronously...");

    // Simulated result
    let status = "completed";
    let output = "Hello from unsandbox!";

    // Print result
    println!("Result status: {}", status);
    println!("Output: {}", output);
}
